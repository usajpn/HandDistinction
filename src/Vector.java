
public class Vector {
	private int x;
	private int y;
	private int data;
	public Vector() {
	}
	
	public void setX(int x) {
		this.x = x;
	}

	public void setY(int y) {
		this.y = y;
	}	
	
	public void setData(int data) {
		this.data = data;
	}	
	
	public int getX() {
		return this.x;
	}
	
	public int getY() {
		return this.y;
	}
	
	public int getData() {
		return this.data;
	}
}
